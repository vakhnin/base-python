"""
Реализовать функцию int_func(),
принимающую слово из маленьких латинских букв и возвращающую его же,
но с прописной первой буквой.
Например, print(int_func(‘text’)) -> Text.
Продолжить работу над заданием.
В программу должна попадать строка из слов, разделенных пробелом.
Каждое слово состоит из латинских букв в нижнем регистре.
Сделать вывод исходной строки,
но каждое слово должно начинаться с заглавной буквы.
Необходимо использовать написанную ранее функцию int_func().
"""


def int_func(word):
    """Функция преобразования первой буквы в строке в верхний регистр"""
    return chr(ord(word[0]) + ord('A') - ord('a')) + word[1:]


def main():
    inp = input('Введите слова из маленьких латинских букв, разделенных пробелами: ')
    print(' '.join(list(map(int_func, inp.split()))))


if __name__ == '__main__':
    main()
